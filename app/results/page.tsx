'use client'

import { useEffect, useState } from 'react'
import { useRouter } from 'next/navigation'
import jsPDF from 'jspdf'
import { supabase } from '@/lib/supabase'

interface Supplement {
  name: string
  dose: string
  timing: string
  reason: string
  warning?: string | null
  study?: string | null
}

interface StackResult {
  supplements: Supplement[]
  summary: string
}

export default function Results() {
  const router = useRouter()
  const [stack, setStack] = useState<StackResult | null>(null)
  const [loading, setLoading] = useState(true)
  const [error, setError] = useState('')
  const [saved, setSaved] = useState(false)
  const [isPro, setIsPro] = useState(false)
  const [userEmail, setUserEmail] = useState('')
  const [loadingMessage, setLoadingMessage] = useState('Analysing your profile...')

  const loadingMessages = [
    'Analysing your profile...',
    'Reviewing the latest research...',
    'Checking for interactions...',
    'Building your personalised stack...',
    'Almost ready...',
  ]

  useEffect(() => {
    let i = 0
    const interval = setInterval(() => {
      i++
      if (i < loadingMessages.length) {
        setLoadingMessage(loadingMessages[i])
      }
    }, 3000)

    const init = async () => {
      try {
        const { data: { user } } = await supabase.auth.getUser()
        if (user) {
          setUserEmail(user.email || '')
          const { data } = await supabase
            .from('subscribers')
            .select('id')
            .eq('user_id', user.id)
            .single()
          if (data) setIsPro(true)
        }

        const stored = localStorage.getItem('stackForm')
        if (!stored) {
          router.push('/intake')
          return
        }
        const formData = JSON.parse(stored)
        const response = await fetch('/api/generate', {
          method: 'POST',
          headers: { 'Content-Type': 'application/json' },
          body: JSON.stringify(formData),
        })
        if (!response.ok) throw new Error('Failed to generate stack')
        const data = await response.json()
        setStack(data)
      } catch (err) {
        setError('Something went wrong. Please try again.')
        console.error(err)
      } finally {
        clearInterval(interval)
        setLoading(false)
      }
    }

    init()
    return () => clearInterval(interval)
  }, [])

  const handleUpgrade = async () => {
    const { data: { user } } = await supabase.auth.getUser()
    if (!user) {
      router.push('/auth?returnTo=/results')
      return
    }
    const response = await fetch('/api/checkout', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ userEmail: user.email }),
    })
    const data = await response.json()
    if (data.url) window.location.href = data.url
  }

  const downloadPDF = async () => {
    if (!stack) return
    if (!isPro) { handleUpgrade(); return }

    const { data: { user } } = await supabase.auth.getUser()
    if (user) {
      const stored = localStorage.getItem('stackForm')
      const formData = stored ? JSON.parse(stored) : {}
      await supabase.from('stacks').insert({
        user_id: user.id,
        form_data: formData,
        stack_data: stack,
      })
      setSaved(true)
    }

    const pdf = new jsPDF('p', 'mm', 'a4')
    const pageWidth = pdf.internal.pageSize.getWidth()
    const margin = 20
    const contentWidth = pageWidth - margin * 2
    let y = 20

    const addText = (text: string, fontSize: number, isBold: boolean, color: [number, number, number]) => {
      pdf.setFontSize(fontSize)
      pdf.setFont('helvetica', isBold ? 'bold' : 'normal')
      pdf.setTextColor(color[0], color[1], color[2])
      const lines = pdf.splitTextToSize(text, contentWidth)
      lines.forEach((line: string) => {
        if (y > 270) { pdf.addPage(); y = 20 }
        pdf.text(line, margin, y)
        y += fontSize * 0.4
      })
      y += 2
    }

    addText('YOUR SUPPLEMENT STACK', 10, false, [34, 197, 94])
    addText('Personalised Protocol', 22, true, [10, 10, 10])
    y += 4
    addText(stack.summary, 11, false, [80, 80, 80])
    y += 6

    stack.supplements.forEach((supp, i) => {
      if (y > 240) { pdf.addPage(); y = 20 }
      addText(String(i + 1) + '. ' + supp.name, 14, true, [10, 10, 10])
      pdf.setFillColor(34, 197, 94)
      pdf.roundedRect(margin, y - 1, 60, 6, 2, 2, 'F')
      pdf.setFontSize(9)
      pdf.setFont('helvetica', 'bold')
      pdf.setTextColor(10, 10, 10)
      pdf.text(supp.dose, margin + 2, y + 3.5)
      y += 8
      addText('When to take: ' + supp.timing, 10, false, [34, 197, 94])
      addText(supp.reason, 10, false, [60, 60, 60])
      if (supp.warning) {
        y += 1
        addText('Warning: ' + supp.warning, 9, false, [161, 120, 0])
      }
      if (supp.study) {
        addText('Research: ' + supp.study, 9, false, [34, 197, 94])
      }
      y += 6
    })

    y += 4
    addText('Generated by StackBuilder AI', 9, false, [150, 150, 150])
    pdf.save('my-supplement-stack.pdf')
  }

  const getAmazonLink = (name: string) => {
    return 'https://www.amazon.co.uk/s?k=' + encodeURIComponent(name) + '&tag=jusscomfy05-21'
  }

  if (loading) {
    return (
      <main className="min-h-screen bg-black text-white flex flex-col items-center justify-center px-4">
        <div className="text-center">
          <div className="w-12 h-12 border-2 border-green-400 border-t-transparent rounded-full animate-spin mx-auto mb-6" />
          <p className="text-green-400 font-medium text-lg mb-2">{loadingMessage}</p>
          <p className="text-gray-600 text-sm">This usually takes 10-20 seconds</p>
        </div>
      </main>
    )
  }

  if (error) {
    return (
      <main className="min-h-screen bg-black text-white flex flex-col items-center justify-center px-4">
        <div className="text-center">
          <p className="text-red-400 font-medium text-lg mb-4">{error}</p>
          <button onClick={() => router.push('/intake')} className="bg-green-400 text-black font-semibold px-8 py-3 rounded-xl hover:bg-green-300 transition-colors">
            Try again
          </button>
        </div>
      </main>
    )
  }

  return (
    <main className="min-h-screen bg-black text-white px-4 py-12">
      <div className="max-w-2xl mx-auto">

        <div className="mb-10">
          <p className="text-green-400 text-sm font-medium tracking-widest uppercase mb-3">Your personalised protocol</p>
          <h1 className="text-4xl font-bold mb-4">Your supplement stack</h1>
          <p className="text-gray-400 text-lg leading-relaxed">{stack?.summary}</p>
        </div>

        <div className="space-y-4 mb-10">
          {stack?.supplements.map((supp, i) => (
            <div key={i} className="bg-gray-900 border border-gray-800 rounded-2xl p-6">
              <div className="flex items-start justify-between mb-3">
                <h3 className="text-xl font-bold">{supp.name}</h3>
                <span className="bg-green-400 text-black text-xs font-semibold px-3 py-1 rounded-full ml-4 shrink-0">{supp.dose}</span>
              </div>
              <p className="text-green-400 text-sm font-medium mb-3">Timing: {supp.timing}</p>
              <p className="text-gray-400 text-sm leading-relaxed mb-3">{supp.reason}</p>
              {supp.warning && (
                <div className="bg-yellow-400/10 border border-yellow-400/20 rounded-xl px-4 py-3 mb-3">
                  <p className="text-yellow-400 text-xs font-medium">Warning: {supp.warning}</p>
                </div>
              )}
              <div className="flex gap-3 flex-wrap mt-2">
                <a href={getAmazonLink(supp.name)} target="_blank" rel="noopener noreferrer" className="inline-block text-xs font-medium text-gray-400 hover:text-white border border-gray-700 hover:border-gray-500 px-3 py-2 rounded-lg transition-colors">Buy on Amazon</a>
                {supp.study && (
                  <a href={supp.study} target="_blank" rel="noopener noreferrer" className="inline-block text-xs font-medium text-green-400 hover:text-green-300 border border-green-400/30 hover:border-green-400 px-3 py-2 rounded-lg transition-colors">View research</a>
                )}
              </div>
            </div>
          ))}
        </div>

        {!isPro && (
          <div className="bg-gray-900 border border-green-400/30 rounded-2xl p-6 mb-6">
            <p className="text-green-400 text-sm font-medium uppercase tracking-widest mb-2">Upgrade to Pro</p>
            <h3 className="text-xl font-bold mb-2">Take your health further</h3>
            <p className="text-gray-400 text-sm mb-1">Pro unlocks your full coaching experience:</p>
            <ul className="text-gray-400 text-sm mb-4 space-y-1">
              <li>Daily AI check-ins that adapt your plan</li>
              <li>Workout builder + progression tracking</li>
              <li>Streaks, progress charts and photos</li>
              <li>Supplement timing reminders</li>
              <li>PDF export and shareable protocol link</li>
              <li>Bloodwork upload for precision recommendations</li>
            </ul>
            <button onClick={handleUpgrade} className="w-full bg-green-400 text-black font-semibold py-3 rounded-xl hover:bg-green-300 transition-colors">Upgrade to Pro - 14.99/month</button>
            <p className="text-gray-600 text-xs text-center mt-2">or 99/year (45% off)</p>
          </div>
        )}

        <div className="border-t border-gray-800 pt-8 space-y-3">
          {isPro ? (
            <button onClick={downloadPDF} className="w-full bg-green-400 text-black font-semibold py-4 rounded-xl hover:bg-green-300 transition-colors">
              {saved ? 'Saved! Downloading PDF...' : 'Save as PDF'}
            </button>
          ) : (
            <button onClick={handleUpgrade} className="w-full bg-gray-800 border border-gray-700 text-gray-400 font-semibold py-4 rounded-xl hover:border-green-400 hover:text-green-400 transition-colors">Save as PDF (Pro only)</button>
          )}
          {isPro && (
            <button onClick={() => router.push('/dashboard')} className="w-full bg-transparent border border-gray-700 text-gray-300 font-medium py-4 rounded-xl hover:border-gray-500 transition-colors">View my dashboard</button>
          )}
          {isPro && (
  <button onClick={async () => {
    const { data: { user } } = await supabase.auth.getUser()
    if (!user || !stack) return
    const stored = localStorage.getItem('stackForm')
    const formData = stored ? JSON.parse(stored) : {}
    const { data: saved } = await supabase.from('stacks').insert({ user_id: user.id, form_data: formData, stack_data: stack }).select().single()
    if (saved) {
      const res = await fetch('/api/share-stack', { method: 'POST', headers: { 'Content-Type': 'application/json' }, body: JSON.stringify({ stackId: saved.id, userId: user.id }) })
      const data = await res.json()
      if (data.shareId) {
        await navigator.clipboard.writeText(window.location.origin + '/share/' + data.shareId)
        alert('Share link copied to clipboard!')
      }
    }
  }} className="w-full bg-transparent border border-gray-700 text-gray-300 font-medium py-4 rounded-xl hover:border-green-400 hover:text-green-400 transition-colors">Share my stack (copy link)</button>
)}
          <button onClick={() => router.push('/intake')} className="w-full bg-transparent border border-gray-700 text-gray-300 font-medium py-4 rounded-xl hover:border-gray-500 transition-colors">Retake the quiz</button>
        </div>

      </div>
    </main>
  )
}
